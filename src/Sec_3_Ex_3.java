/* Java program to demonstrate some
   logical operations
 */
public class Sec_3_Ex_3 {
    public static void main(String[] args){
        boolean a = true;
        boolean b = false;

        System.out.println("a && b : " + (a && b));
        System.out.println("a || b : " + (a || b));
        System.out.println("!a: " + !a + "\n!b: " + !b);
    }
}
