
/* Java program to perform relational operations on the
   integers 40 and 20
   */
public class Sec_3_Ex_2 {
    public static void main(String[] args){
        int a = 40;
        int b = 20;

        System.out.println("a == b : " + (a == b) );
        System.out.println("a != b : " + (a != b) );
        System.out.println("a > b : "+ (a > b) );
        System.out.println("a < b : " + (a < b) );
        System.out.println("a >= b : " + (a >= b));
        System.out.println("a <= b : " + (a <= b));
    }
}
